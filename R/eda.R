## Course ID
ggplot(data = HxMx) +
  geom_bar(mapping = aes(x = course_id)) +
  scale_x_discrete(name = " ") +
  scale_y_continuous(name=" ", labels = comma) +
  theme(axis.title.x = element_blank(),
        axis.text.x = element_text(angle=90, vjust=0.5)) +
  labs(title = "Course ID")

